public class Contoh01 {
    public static void main(String[] args) {
        //PERULANGAN FOR
        int x;
        for(x = 1; x <= 10; x++)
        {
            System.out.println("SMK TELKOM MALANG");
        }
        //PERULANGAN WHILE
        int i = 1;
                while(i <= 5)
                {
                    System.out.println("SMK TELKOM MALANG");
                    i++;
                }
                
        //PERULANGAN DO WHILE
        int j = 1;
        do
        {
            System.out.println("SMK TELKOM MALANG");
            j++;
        }
        while(j <= 5);
    }
}
